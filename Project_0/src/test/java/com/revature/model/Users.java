package com.revature.model;

import java.io.Serializable;

public class Users implements Serializable {

	private String firstName;
	private String lastName;
	private int userId;
	private String emailAddress;
	private String password;
	public Users(String firstName, String lastName, int userId, String emailAddress, String password) {
		super();
		this.firstName = firstName;
		this.lastName = lastName;
		this.userId = userId;
		this.emailAddress = emailAddress;
		this.password = password;
	}
	
	public Users(String firstName, String lastName,String emailAddress, String password) {
		super();
		this.firstName = firstName;
		this.lastName = lastName;
	
		this.emailAddress = emailAddress;
		this.password = password;
	}
	
	
	
	@Override
	public String toString() {
		return "Users [firstName=" + firstName + ", lastName=" + lastName + ", userId=" + userId + ", emailAddress="
				+ emailAddress + ", password=" + password + "]";
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getEmailAddress() {
		return emailAddress;
	}
	public void setEmailAddress(String emailAddress) {
		this.emailAddress = emailAddress;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((emailAddress == null) ? 0 : emailAddress.hashCode());
		result = prime * result + ((firstName == null) ? 0 : firstName.hashCode());
		result = prime * result + ((lastName == null) ? 0 : lastName.hashCode());
		result = prime * result + ((password == null) ? 0 : password.hashCode());
		result = prime * result + userId;
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Users other = (Users) obj;
		if (emailAddress == null) {
			if (other.emailAddress != null)
				return false;
		} else if (!emailAddress.equals(other.emailAddress))
			return false;
		if (firstName == null) {
			if (other.firstName != null)
				return false;
		} else if (!firstName.equals(other.firstName))
			return false;
		if (lastName == null) {
			if (other.lastName != null)
				return false;
		} else if (!lastName.equals(other.lastName))
			return false;
		if (password == null) {
			if (other.password != null)
				return false;
		} else if (!password.equals(other.password))
			return false;
		if (userId != other.userId)
			return false;
		return true;
	}


}